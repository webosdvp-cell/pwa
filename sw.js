function getRandFact() {
  return FACTS[Math.floor(Math.random() * FACTS.length)];
}

self.addEventListener('install', e => self.skipWaiting());
self.addEventListener('activate', e => clients.claim());

// Cache map tiles automatically
self.addEventListener('fetch', function(e) {
  var u = e.request.url;
  if ((u.includes('.png') || u.includes('.jpg')) &&
      (u.includes('tile.') || u.includes('/tiles/'))) {
    e.respondWith(
      caches.open(TC).then(function(cache) {
        return cache.match(e.request).then(function(cached) {
          if (cached) return cached;
          return fetch(e.request).then(function(r) {
            if (r.ok) cache.put(e.request, r.clone());
            return r;
          }).catch(function() {
            return cached || new Response('', { status: 503 });
          });
        });
      })
    );
  }
});

// Receive message from main page to send notification
self.addEventListener('message', function(e) {
  if (e.data === 'CHECK_NOTIF') {
    self.registration.showNotification('GeoGo — Факт о Земле', {
      body: getRandFact(),
      icon: '/icon.png',
      badge: '/icon.png',
      vibrate: [100, 50, 100],
      tag: 'geogo-fact',
      requireInteraction: false,
    });
  }
});

self.addEventListener('notificationclick', function(e) {
  e.notification.close();
  e.waitUntil(clients.openWindow('/'));
});
